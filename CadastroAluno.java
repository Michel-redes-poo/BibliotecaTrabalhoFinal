    private void ExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ExcluirActionPerformed
        Excluir.addMouseListener(new MouseAdapter(){
            @Override
            public void mouseClicked(MouseEvent e){
                DefaultListModel listaAlunos = new DefaultListModel();
                if (AlunoDAO.tamanhoDaLista() > 0) {
                    AlunoDAO.excluirAluno(ListaAlunos.getSelectedIndex());
                    
                    JOptionPane.showMessageDialog(null, "Aluno Exclu√≠do!");
                    for (int k = 0; k < AlunoDAO.tamanhoDaLista(); k++){
                    listaAlunos.addElement(AlunoDAO.getAlunos(k));
                    };
                }
                ListaAlunos.setModel(listaAlunos);
            }            
        });
            
    }//GEN-LAST:event_ExcluirActionPerformed

    private void VisualizarAlunosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_VisualizarAlunosActionPerformed
        DefaultListModel alunos = new DefaultListModel();
        if (AlunoDAO.tamanhoDaLista()> 0){
            for (int k = 0; k < AlunoDAO.tamanhoDaLista(); k++){
                alunos.addElement(AlunoDAO.getAlunos(k));
            }            
        }else{
            alunos.addElement("N√£o existem alunos cadastrados!");
        }
        ListaAlunos.setModel(alunos);
    }//GEN-LAST:event_VisualizarAlunosActionPerformed

